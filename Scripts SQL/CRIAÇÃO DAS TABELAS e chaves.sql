CREATE TABLE VAGA(
	ID_VAGA INT NOT NULL,
	SECAO_VAGA VARCHAR(2) NOT NULL,
	ANDAR_VAGA INT NOT NULL,
	ESTADO_VAGA VARCHAR(2) NOT NULL,
	CIDADE_VAGA VARCHAR(20) NOT NULL,
	CEP_VAGA VARCHAR(9) NOT NULL,
	HORARIO_RESERVA TIME NOT NULL,
	PRECO_VAGA FLOAT NOT NULL,
	PRIMARY KEY(ID_VAGA)
	);

CREATE TABLE EMPRESA(
	EMPRESA_RESP VARCHAR(30) NOT NULL,
	CNPJ_EMPRESA VARCHAR(18) NOT NULL,
	PRIMARY KEY(CNPJ_EMPRESA)
	);
	
CREATE TABLE ADMINISTRADOR(
	NOME VARCHAR(30) NOT NULL,
	EMAIL VARCHAR(30) NOT NULL,
	ID_ADMINISTRADOR SERIAL NOT NULL,
	PRIMARY KEY(ID_ADMINISTRADOR)
	);

CREATE TABLE CONDUTOR(
	NOME VARCHAR(30) NOT NULL,
	CPF_CONDUTOR VARCHAR(14) NOT NULL,
	EMAIL_CONDUTOR VARCHAR(30) NOT NULL,
	TEL_CONDUTOR VARCHAR(15) NOT NULL,
	PRIMARY KEY(CPF_CONDUTOR)
	);
	

